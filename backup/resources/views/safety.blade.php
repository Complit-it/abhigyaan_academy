					<h2>RULES OF ROAD</h2>
						<p><strong>Keep left</strong></p>
						<p>How far from the left side of the road you should drive depends upon the road condition and the type of traffic on it. But, as a driver, you must drive sufficiently to the left</p>
						<p><strong>Lane driving</strong></p>
						<p>Every road has lanes, whether marked or not. Where they are not marked, divide the road mentally into appropriate lanes. Where it is marked, drive within your lane. Change only if it is safe to do so and after giving a proper signal. A good driver never weaves in and out of a lane. In a traffic hold-up, don’t try to get ahead by jumping the queue or by cutting into another lane.</p>
						<p><strong>Changing Lanes</strong></p>
						<p>If you need to move into another lane, check through your rearview mirror and look over your shoulder for the traffic following you, if it is safe, give the proper signal and then change lanes. After you have changed your lane or overtaken, make sure that the indicator given to change lane is switched off.</p>
						<p><strong>Turning left and right</strong></p>
						<p>Whether you are going to make a left or right turn, you should be in the correct lane well before reaching the intersection. You should be in the lane closest to the direction in which you are going to turn. Never turn from the wrong lane across another lane of traffic. This unexpected move can be dangerous.</p>
						<p><strong>Reverse driving</strong></p>
						<p>Reverse driving could be dangerous. Make sure you have rear side view mirrors on your left and right doors in addition to the one fitted inside. Before you reverse, make sure there are no pedestrians, especially children, or any other impediments on the road behind you. Be particularly careful of the blind areas that are obscured from the driver’s seat. Never reverse from a side road onto a main road.</p>
						<p><strong>U Turn</strong></p>
						<p>After ensuring that there is no prohibition to take a U-turn, also make sure:</p>
						<ul>
						<li>That the road is wide enough for this manoeuvre</li>
						<li>That there is no traffic approaching from either side and you have full visibility of the road on both sides.</li>
						</ul>
						<p><strong>Speed</strong></p>
						<p>Driving at a high speed does not give you sufficient reaction time to observe hazards. Other road users too, get less time to react. The higher the speed, the greater the stopping distance and larger the chances of an accident.</p>
						<p>The golden rule for speed:</p>
						<p>Drive at a speed that will allow you to stop well within the distance you can see clearly</p>
						<p><strong>Following Distance</strong></p>
						<p>Always maintain sufficient distance between the vehicle you are driving and the vehicle in front of you in order to avoid collision in case the vehicle ahead slows down or stops suddenly. Avoid applying the brakes abruptly unless it is necessary to do so for safety reasons.</p>
						<p>The law also provides that the driver if a motor vehicle moving behind another vehicle shall keep at a sufficient from the other vehicle to avoid collision if the vehicle in front should suddenly slow down or stop.</p>
						<p><strong>Overtaking</strong></p>
						<p>One of the major causes of road accidents is callous overtaking.  So, you have an important responsibility while overtaking.  Be careful while you observe oncoming vehicles in the overtaking exercise.  You must only overtake when you are sure that after completing the maneuver you can return to your side of the road without obstructing the oncoming traffic or getting in the way of vehicles you are overtaking. </p>
						<p>Take extreme care when overtaking on roads with an undivided carriageway.  Impatient drivers may not want to reduce their speed and may remain in the middle of the road resulting in head-on collisions.</p>
						<p>When overtaking, keep in mind:</p>
						<ul>
							<li>Overtake from the right of the vehicles you are passing</li>
							<li>Do not overtake if the driver of the vehicle in front of you indicates that he is turning right.</li>
							<li>Never cut onto heavy vehicles. They need more room to slow down and stop.</li>
						</ul>
						<p>Do not attempt to overtake if:</p>
						<ul>
							<li>Your passing is likely to cause inconvenience or danger to another traffic proceeding in any direction.</li>
							<li>You are near a pedestrian crossing, intersection, or railway crossing</li>
							<li>You are on a road where overtaking is not allowed.</li>
							<li>The vehicle you are wanting to overtake has started accelerating</li>
							<li>The vehicle to be overtaken is displaying an ‘L’ plate.</li>
							<li>Emergency vehicle</li>
							<li>VIP entourage</li>
							<li>You cannot see clearly ahead due to the dazzle of oncoming traffic</li>
							<li>A large vehicle is blocking your vision, as there may be slow-moving vehicles in front</li>
						</ul>
						<p>Parking</p>
						<p>The absence of a ‘No Parking’ sign does not necessarily mean that you can park there. Wherever possible, park off the road or in a car park. Normally, a parking space is indicated by a road sign or markings. Look for the sign and park your vehicle accordingly.</p>
