						<h1> Road Safety Tips </h1>
						<h2> Overtaking </h2>
						When overtaking, keep in mind:
						<ul>
						  <li>Overtake from the right of the vehicles you are passing</li>
						  <li>Do not overtake if the driver of the vehicle in front of you indicates that he is turning right.</li>
						  <li>Never cut onto heavy vehicles. They need more room to slow down and stop.</li>
						</ul>
						Do not attempt to overtake if:

						<ul>
						  <li>Your passing is likely to cause inconvenience or danger to another traffic proceeding in any direction.</li>
						  <li>You are near a pedestrian crossing, intersection, or railway crossing</li>
						  <li>You are on a road where overtaking is not allowed.</li>
						  <li>The vehicle you are wanting to overtake has started accelerating</li>
						  <li>The vehicle to be overtaken is displaying an ‘L’ plate.</li>
						  <li>Emergency vehicle</li>
						  <li>VIP entourage</li>
						  <li>You cannot see clearly ahead due to the dazzle of oncoming traffic</li>
						  <li>A large vehicle is blocking your vision, as there may be slow-moving vehicles in front</li>
						</ul>
						<h2> Parking </h2>
						The absence of a ‘No Parking’ sign does not necessarily mean that you can park there. Wherever possible, park off the road or in a car park. Normally, a parking space is indicated by a road sign or markings. Look for the sign and park your vehicle accordingly.
						<h2> Road Safety Tips for Pedestrians </h2>
						Walk on the footpath - 50% of those killed in road accidents are pedestrians.
						Always remain alert while on the road:

						<ul>
						  <li>Walk on any side of road if there are footpaths.</li>
						  <li>On roads having no footpath, walk on the extreme right side facing the oncoming traffic.</li>
						  <li>Always use Zebra Crossing, Foot Over-bridge &amp; Subways to cross the road.</li>
						  <li>Cross the road when the vehicles are at a safe distance.</li>
						  <li>Wear light-coloured dresses during the night.</li>
						</ul>
						<h2> Safety Tips for Crossing the Road </h2>
						<ul>
						  <li>Do not cross the road in a hurry or by running.</li>
						  <li>Never cross the road in front of or in between parked vehicles.</li>
						  <li>It is very dangerous to cross the road at blind corners, turnings, etc. where you are not visible to vehicle drivers.</li>
						  <li>Do not jump over the railings to cross the road.</li>
						</ul>
						<h2> Cycle Safely </h2>
						10% of the persons being killed in road accidents are cyclists.
						Always:

						<ul>
						  <li>Cycle must be fitted with standard gadgets – bell, brakes, rearview mirror, both front &amp; black mudguard painted white, reflective tapes affixed at the front &amp; back.</li>
						  <li>Use service road/cycle track, if available. The cycle should be on the extreme left side of the road.</li>
						  <li>Avoid busy roads.</li>
						  <li>Keep a safe distance from fast motorized vehicles.</li>
						  <li>Give proper indications before stopping or turning.</li>
						</ul>
						Never:

						<ul>
						  <li>Do not indulge in any kind of stunts.</li>
						  <li>Do not load the cycle with another person or heavy goods.</li>
						  <li>Do not ride holding on to fast-moving vehicles.</li>
						  <li>Do not ride parallel to another cycle. </li>
						</ul>

						<h1>TRAVELING IN BUS</h1>
							<h2>Always</h2>
							<ul>
							  <li>Get up early and start from home on time.</li>
							  <li>Board the bus from the designated bus stop in a queue.</li>
							  <li>Behave properly inside the bus.</li>
							  <li>Hold on to the railings of the bus.</li>
							  <li>Alight only at the designated bus stop.</li>
							  <li>Get down only when the bus has completely stopped.</li>
							</ul>
							<h2>Never</h2>
							<ul>
							  <li>Rush or run to catch the bus.</li>
							  <li>Stand on the steps of the bus.</li>
							  <li>Make noise that may distract the driver.</li>
							  <li>Put any part of the body outside the bus.</li>
							  <li>Alight from a moving bus.</li>
							  <li>Cross in front of the bus you have alighted.</li>
							</ul>
						<h1>RIDE TWO-WHEELER SAFELY</h1>
						<p>28% of the persons being killed in road accidents are two-wheeler riders.</p>
						<h2>Always</h2>
						<ul>
						  <li>Wear a full mask good quality BIS-approved helmet.</li>
						  <li>Ensure that the pillion rider also wears a helmet of the same quality.</li>
						  <li>Fasten the helmet tightly.</li>
						  <li>Check the inflation of both front and rear tires.</li>
						  <li>Ensure the front and rear lights are functional.</li>
						  <li>Give proper indications before turning.</li>
						  <li>Switch off your indicators after turning.</li>
						  <li>Use front and rear brakes simultaneously.</li>
						  <li>Ride in the middle lane.</li>
						  <li>Keep a safe distance from the vehicle ahead.</li>
						</ul>
						<h2>Never</h2>
						<ul>
						  <li>Indulge in zig-zag riding.</li>
						  <li>Overtake from the blind corners of a heavy vehicle or from the left side. It is highly dangerous.</li>
						  <li>Apply brakes suddenly.</li>
						  <li>Carry heavy loads or more than one person as a pillion rider.</li>
						  <li>Use a cell phone while riding.</li>
						  <li>Ride in the bus lane.</li>
						  <li>Allow a minor to ride.</li>
						</ul>

						<h1>Negotiating an Intersection Judiciously</h1>
						<p>Almost 50% of collisions occur at intersections.</p>
						<ul>
							<li>Choose the left lane if you want to turn left, the middle lane if you want to go straight, and the right lane if turning right, at least 100 m in advance.</li>
							<li>Give proper indication before you turn.</li>
							<li>Slow down while approaching an intersection.</li>
							<li>Stop before the stop line at the red light.</li>
							<li>Even if the signal allows you to go, proceed slowly and cautiously.</li>
							<li>While approaching an unmanned intersection cover the brake and be prepared to stop.</li>
						</ul>

						<h1>Negotiating a Round About</h1>
						<ul>
							<li>Choose your lane as per your exit, at least 100m in advance.</li>
							<li>Slow down while approaching a round about.</li>
							<li>Enter the round about at an angle.</li>
							<li>Give way to traffic on your right.</li>
							<li>Merge slowly with the traffic inside the round about.</li>
							<li>Move towards your exit gradually giving proper indications.</li>
							<li>Be watchful of a pedestrian or an animal that may suddenly appear.</li>
						</ul>

						<h1>While Overtaking</h1>
						<ul>
							<li>Never overtake from left, always overtake from right.</li>
							<li>Make clear your intentions of overtaking your fellow drivers.</li>
							<li>Give proper indications.</li>
							<li>Be watchful of a pedestrian or an animal that may suddenly appear in front of the vehicle you are overtaking.</li>
							<li>On roads having two-way traffic overtake only when the oncoming vehicle is at a safe distance.</li>
						</ul>

						<h1>While Turning</h1>
						<ul>
							<li>Give proper indications.</li>
							<li>Start changing lane only when the vehicles behind you have understood your intentions.</li>
							<li>Slow down while approaching the turning point.</li>
							<li>Turn only when the vehicle coming from the opposite direction is at a safe distance.</li>
							<li>The vehicle coming from the opposite direction has the right of way.</li>
							<li>Watch out for pedestrians as well as vehicles coming from your right side.</li>
						</ul>

						<h2>Right of Way</h2>
						<ul>
						  <li>The children and the disabled have the right of way.</li>
						  <li>Pedestrians have the first right of way at unmanned intersections.</li>
						  <li>At unmanned intersections traffic on the right has the right of way.</li>
						  <li>At a roundabout, traffic on your right has the right of way.</li>
						  <li>Traffic on the major road has the right of way.</li>
						  <li>On hilly and steep roads vehicles going uphill have the right of way.</li>
						  <li>Emergency vehicles like fire brigade, ambulance, and police have the right of way.</li>
						  <li>These vehicles, while on emergency duty, can jump a red right, drive in nonentry areas, or on the wrong side. </li>
						  <li>These vehicles should be given priority and clear passage.</li>
						</ul>
						<h2>Driving at Night</h2>
						<ul>
						  <li>Be alert: pedestrians, bicycles, animals, and hand-drawn vehicles that travel without light are difficult to be seen.</li>
						  <li>Drive at a slow speed to react safely and stop in time.</li>
						  <li>Turn on the headlights when it is dark.</li>
						  <li>Drive with dipped headlights in places where the roads are amply lit.</li>
						  <li>Keep the headlights clear and clean and check them frequently.  Keep your windscreen clean because a dirty windscreen can impair your vision.</li>
						  <li>Avoid looking at the dazzling light if an oncoming vehicle is driving at a high beam and gradually slow down your speed.</li>
						  <li>To reduce the glare of lights following you, switch your interior rearview mirror to the night position or slightly tilt it or tilt the exterior rearview mirrors.</li>
						  <li>Before overtaking ensure that the oncoming vehicle is at a safe distance.</li>
						</ul>
						<h2>Driving in Rain</h2>
						<ul>
						  <li>Check the working of wipers, windscreen washing fluid system, all vehicle lights, tires, and exhaust pipe.</li>
						  <li>Be careful and reduce your speed as people may run across the roads in panic.</li>
						  <li>Be considerate to other road users, and avoid splashing water.</li>
						  <li>On slippery roads drive carefully and slowly.  In case the vehicle slips, stop acceleration and press the clutch, hold the steering in a straight position, and do not break.  Wait till you regain the grip.</li>
						</ul>
						<h2>Driving in Foggy Conditions</h2>
						<ul>
						  <li>Drive slow as distances are hard to judge and low visibility decreases your reaction time.</li>
						  <li>Do not hit your brakes in panic, you may get hit from the back.</li>
						  <li>Turn on your wipers and defroster and turn off the music.</li>
						  <li>Drive with dipped headlights, high beams produce too much glare in fog.</li>
						  <li>Use the central verge of the road to guide you.</li>
						  <li>Honk periodically to let other drivers know you are there.</li>
						  <li>Never attempt overtaking.</li>
						</ul>

						<h1>Important Road Safety Tips</h1>
							<h2>Do Not Drive When Drunk</h2>
							<ul>
							  <li>Alcohol slows down the mental process.</li>
							  <li>It increases confidence but decreases performance.</li>
							  <li>It affects the brain’s ability to control and coordinate the body’s movement.</li>
							  <li>It slows down the reflex and hence the reaction time increases.</li>
							  <li>It impairs the ability to judge speed and distance.</li>
							  <li>It also impairs vision and hearing.</li>
							</ul>
							<h2>Seatbelt Saves</h2>
							<ul>
							  <li>Seat belt saves life and reduces the severity of injuries.</li>
							  <li>With a seat belt tied around, you become a part of the vehicle, rather than a loose object that can be tossed around inside in a crash or thrown outside.</li>
							  <li>If you are thrown out of a vehicle in a crash, your chances of being killed are 25 times greater than if you stay inside.</li>
							  <li>If your vehicle goes out of control, with the seat belt on you may be able to regain control. Without it, you may not even be able to stay in the driver’s seat.</li>
							</ul>
							<h2>How to React in Case of an Accident</h2>
							<ul>
							  <li>Always carry a basic emergency kit in your vehicle, containing flares and first aid supplies.</li>
							  <li>If involved in a crash you must stop, regardless of the extent of the damage.</li>
							  <li>It is a criminal offense to leave the scene of an accident involving a fatality or a personal injury.</li>
							  <li>Exchange information with other drivers involved. Give your name, address, etc. to the other drivers and police on the scene.</li>
							  <li>If a parked vehicle or property other than a vehicle is damaged or if a domestic animal is injured, try to locate the owner or notify the police.</li>
							  <li>Do not stop at an accident scene unless you are involved or emergency help is needed. Otherwise, keep your attention on driving and the directions given by traffic police.</li>
							  <li>Shift the injured immediately to the hospital in any vehicle available.</li>
							</ul>
							<h2>Emergency First-Aid</h2>
							<ul>
							  <li>Do not move an injured person unless it is absolutely necessary.</li>
							  <li>If you must move an injured person, keep the back and neck as straight as possible by putting your arms under the back, and gently support the neck with your upper arms.</li>
							  <li>Take hold of the clothing with your hands, and pull the victim headfirst away from danger.</li>
							  <li>Check to see if the injured is breathing. If the person is not breathing and you are trained in cardiopulmonary resuscitation (CPR), begin administering CPR or mouth-to-mouth breathing.</li>
							</ul>
							<h2>Traffic Control Devices</h2>
							<p>Traffic control devices are all signs, signals, markings and devices placed on or adjacent to a road or highway, to regulate, warn or guide traffic to promote road safety. Every road user must be aware of these devices.</p>
							<p>Traffic control devices comprise of :</p>
							<ul>
						  <li>Traffic signs</li>
						  <li>Road markings</li>
						  <li>Traffic signals</li>
						  <li>Miscellaneous traffic control devices such as barricades, traffic cones, central refuges, speed breakers, bollards, reflective devices etc.</li>
						</ul>


						<h2>What Do Traffic Signals Indicate?</h2>
						  <ul>
							<li><strong>RED</strong> means STOP</li>
							<ul>
							  <li>Wait behind the stop line or crosswalk.</li>
							  <li>If there are no lines, stop before the traffic light at the intersection so that traffic light is clearly visible.</li>
							  <li>Wait until a green signal appears before proceeding.</li>
							</ul>
							<li><strong>AMBER</strong> means CAUTION</li>
							<ul>
							  <li>You may move on if the amber appears after you have already crossed the stop line or when you feel that your stopping may cause an accident. In any case, be careful.</li>
							</ul>
							<li><strong>GREEN</strong> means GO</li>
							<ul>
							  <li>Proceed ahead ensuring that the way is clear.</li>
							  <li>You can make a right or left turn if not prohibited by signs, but take special care and give way to pedestrians crossing the road.</li>
							</ul>
