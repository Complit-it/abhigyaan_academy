  </li>
  <li>
    <h2>Brake fluid</h2>
    <p>While you’re under the bonnet, check the fluid level in your car’s braking system. This fluid is essential for making sure your brakes work, so don’t take any chances with it. Your car’s manual will tell you where to look in the engine bay. Much like the coolant, you’re looking for a reservoir with markers showing you the lowest recommended level of fluid. If the brake fluid level is below that marker, top it up by unscrewing the lid and adding the recommended type of fluid. Again, your car’s manual will tell you what kind to use.</p>
  </li>
  <li>

  <h2>Tyre tread depth</h2>
  <p>Worn tyres can seriously affect your car’s handling, braking and safety as a whole. Before a long journey, check that your tyre treads have a minimum depth of 1.6mm across the central three-quarters where it hits the road using a measuring gauge. If your tread has worn down to between 1.6mm and 3mm in depth, consider replacing your tyres before your trip.</p>
</div>
<div>
  <h2>Fuel level</h2>
  <p>Most people are keen to get on the road and make good progress, but filling up with a tank of fuel at or near the start of your journey could save you time (and reduce stress) later on. Knowing that you’ve got a full tank gives you peace of mind and could stop you from having to drive around an unfamiliar location towards the end of your journey, desperately hunting for a fuel station.</p>
  <p>If you have a plug-in hybrid or electric car, make sure it’s fully charged before setting off. Some also allow you to set a timer to pre-cool or pre-heat the car while it’s charging. It’s worth doing this because it reduces the amount of battery power you use once you start moving.</p>
</div>
<div>
  <h2>Emergency supplies</h2>
  <p>Pack anything that may be useful in an emergency if you were to break down. A red warning triangle is highly recommended in order to alert other drivers of your presence. It’s always worth keeping some spare clothes and snacks in the car in case you get stuck somewhere for a while. If you’re driving in Europe you may need to pack a few additional items – in France, for example, you are required by law to have two warning triangles, a reflective jacket and a first-aid kit in your car.</p>
</div>
<div>
  <h2>Battery</h2>
  <p>A petrol or diesel car’s battery will degrade over time and will need to be replaced. Usually, this happens every few years, but it’s important to keep an eye on how it’s performing – if it drops below a certain operating level, your car won’t start, leaving you stranded.</p>
</div>
<div>
  <h2>Have your car serviced regularly</h2>
  <p>The best way to make sure your car is ready for a long journey is by having it regularly serviced. That way you know you’ve done everything you can to keep it running efficiently and safely. Many cars will remind you with a message on the dashboard when a service is due. If in doubt, check your car’s handbook or service record to see when its next check-up is due.</p>
