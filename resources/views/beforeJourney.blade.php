						<h1>Pre-journey checks</h1>
    <p>Pre-journey checks help you to drive more safely, avoid unnecessary breakdowns and make your long trip easier and a lot more enjoyable.</p>
    <ol style="list-style-type: none;">
      <li>
        <h2>Tyre pressures and spare tyre</h2>
        <p>The correct tyre pressures are essential to making sure that your car can break, grip and steer properly. You should always have a spare tyre with you for emergencies. Nowadays for tubeless tyres one can keep the kit to seal any air leakage from the tyre.</p>
      </li>
      <li>
        <h2>Windscreen wipers and screen wash</h2>
        <p>Driving with a dirty or smeared windscreen is frustrating, but it can also be dangerous. Check your windscreen wipers for wear and replace them if needed.</p>
      </li>
      <li>
        <h2>Lights</h2>
        <p>Fully functioning lights are essential for safe travel, not just so you can see clearly but also so that other road users can see you and know your intentions. Before a long journey is a good time to check your headlights, indicators and brake lights.</p>
      </li>
      <li>
        <h2>Air conditioning</h2>
        <p>If you have an air conditioner in your vehicle then it’s a great idea to make sure your air conditioner is in tip-top shape before spending hours at a time in the car. The last thing you want is to get stuck in traffic with no way to cool down.</p>
      </li>
      <li>
        <h2>Oil level</h2>
        <p>Oil is absolutely vital in keeping your car’s engine running smoothly. Running low on oil could cause costly damage or leave you stranded – which is the last thing you want when you’re a long way from home. Traditionally, every car came with a dipstick to allow you to check the oil level yourself. Many modern cars no longer have dipsticks and instead, use the car’s computer to monitor the oil level and display it on the dashboard. You should check your car’s handbook to see if this is the case for you.</p>
      </li>
      <li>
        <h2>Engine coolant</h2>
        <p>Coolant keeps your car’s engine healthy by regulating the temperature of the cooling system. Many newer cars have a closed cooling system which means they don’t need topping up. With older cars, you may need to check the level of coolant yourself and top it up if necessary. You can see the fluid level in a reservoir in the engine bay. If it’s close to, or below, the marker for the minimum level, you’ll need a refill.</p>
      </li>

